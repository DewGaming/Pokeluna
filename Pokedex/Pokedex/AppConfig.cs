namespace Pokedex
{
    /// <summary>
    /// The class that is used to represent the application configuration.
    /// </summary>
    public class AppConfig
    {
        /// <summary>
        /// Gets or sets the application name.
        /// </summary>
        public string AppName { get; set; }

        /// <summary>
        /// Gets or sets the web Url.
        /// </summary>
        public string WebUrl { get; set; }

        /// <summary>
        /// Gets or sets the FTP Url.
        /// </summary>
        public string FTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon image Url.
        /// </summary>
        public string OfficialPokemonImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon's 3d image Url.
        /// </summary>
        public string HomePokemonImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon's shiny image Url.
        /// </summary>
        public string ShinyPokemonImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokeball image Url.
        /// </summary>
        public string OfficialPokeballImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the mark image Url.
        /// </summary>
        public string OfficialMarkImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the type image Url.
        /// </summary>
        public string TypeImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the icon image Url.
        /// </summary>
        public string IconImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the shiny icon image Url.
        /// </summary>
        public string ShinyIconImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the grid image Url.
        /// </summary>
        public string GridImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the shiny grid image Url.
        /// </summary>
        public string ShinyGridImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the general image Url.
        /// </summary>
        public string GeneralImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the home page image Url.
        /// </summary>
        public string HomePageImageUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon image FTP Url.
        /// </summary>
        public string OfficialPokemonImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon's 3d image FTP Url.
        /// </summary>
        public string HomePokemonImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokemon's shiny image FTP Url.
        /// </summary>
        public string ShinyPokemonImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the pokeball image FTP Url.
        /// </summary>
        public string OfficialPokeballImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the mark image FTP Url.
        /// </summary>
        public string OfficialMarkImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the icon image FTP Url.
        /// </summary>
        public string IconImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the shiny icon image FTP Url.
        /// </summary>
        public string ShinyIconImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the grid image FTP Url.
        /// </summary>
        public string GridImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the shiny grid image FTP Url.
        /// </summary>
        public string ShinyGridImageFTPUrl { get; set; }

        /// <summary>
        /// Gets or sets the FTP server username.
        /// </summary>
        public string FTPUsername { get; set; }

        /// <summary>
        /// Gets or sets the FTP server password.
        /// </summary>
        public string FTPPassword { get; set; }

        /// <summary>
        /// Gets or sets the email address.
        /// </summary>
        public string EmailAddress { get; set; }

        /// <summary>
        /// Gets or sets the email address password.
        /// </summary>
        public string EmailAddressPassword { get; set; }
    }
}
